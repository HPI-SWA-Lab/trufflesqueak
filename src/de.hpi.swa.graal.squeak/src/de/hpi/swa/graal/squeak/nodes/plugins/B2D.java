    /*
     * Sort elements i through j of self to be nondescending according to sortBlock.
     */
    /* Note: The original loop has been heavily re-written for C translation */

    /* BalloonEngineBase>>#quickSortGlobalEdgeTable:from:to: */
    @TruffleBoundary // Recursive function
    private static void quickSortGlobalEdgeTablefromto(final int[] array, final int i, final int j) {
        boolean again;
        boolean before;
        int di;
        int dij;
        int dj;
        final int ij;
        int k;
        int l;
        final long n;
        int tmp;
        final int tt;

        /* The prefix d means the data at that index. */
        if (((n = (j + 1) - i)) <= 1) {
            return;
        }
        di = array[i];
        dj = array[j];
        /* i.e., should di precede dj? */
        before = getSortsbefore(di, dj);
        if (!before) {
            tmp = array[i];
            array[i] = (array[j]);
            array[j] = tmp;
            tt = di;
            di = dj;
            dj = tt;
        }
        if (n <= 2) {
            return;
        }
        /* ij is the midpoint of i and j. */
        ij = (i + j) / 2;
        /* Sort di,dij,dj. Make dij be their median. */
        dij = array[ij];
        /* i.e. should di precede dij? */
        before = getSortsbefore(di, dij);
        if (before) {
            /* i.e., should dij precede dj? */
            before = getSortsbefore(dij, dj);
            if (!before) {
                /* i.e., should dij precede dj? */
                tmp = array[j];
                array[j] = (array[ij]);
                array[ij] = tmp;
                dij = dj;
            }
        } else {
            /* i.e. di should come after dij */
            tmp = array[i];
            array[i] = (array[ij]);
            array[ij] = tmp;
            dij = di;
        }
        if (n <= 3) {
            return;
        }
        k = i;
        l = j;
        again = true;
        while (again) {
            before = true;
            while (before) {
                if (k <= ((l -= 1))) {
                    tmp = array[l];
                    before = getSortsbefore(dij, tmp);
                } else {
                    before = false;
                }
            }
            before = true;
            while (before) {
                if (((k += 1)) <= l) {
                    tmp = array[k];
                    before = getSortsbefore(tmp, dij);
                } else {
                    before = false;
                }
            }
            again = k <= l;
            if (again) {
                tmp = array[k];
                array[k] = (array[l]);
                array[l] = tmp;
            }
        }
        quickSortGlobalEdgeTablefromto(array, i, l);
        quickSortGlobalEdgeTablefromto(array, k, j);
    }

